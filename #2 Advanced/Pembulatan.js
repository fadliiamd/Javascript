var x = 15.7;

console.log(Math.ceil(x));
console.log(Math.floor(x));
console.log(Math.round(x));

// Ceil adalah pembulatan ke atas
// floor adalah pembulatan ke bawah
// round adalah pembulatan berdasarkan angka 5, apabila lebih dari 5 maka akan dilakukan pembulatan ke atas
// sedangkan apabila kurang dari 5 maka akan dilakukan pembulatan ke bawah