var i = 0;

while (i < 10) {
    console.log("Ini urutan ke", i);
    i++;
}

// While merupakan salah satu jenis kondisi perulangan,
// kondisi di atas menyatakan variabel i dengan value = 0,
// lalu kondidi perulangan menyatakan bahwa i akan berhenti melakukan perulangan
// apabila value sudah mencapai 10.