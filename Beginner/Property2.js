function panggilMobil() {
    var mobil = {
        brand: 'rolls royce',
        warna: 'hitam doff',
        harga: 20000,
    }
    // cara merubah nilai property
    mobil.tahun = 2019
    console.log(mobil);
}

panggilMobil()

