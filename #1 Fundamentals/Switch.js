function testingSwitch() {
    var nilai = 10;

    switch (nilai) {
        case 10:
            console.log("Sangat Baik");
            break;
        case 8:
            console.log("Baik");
            break;
        case 6:
            console.log("Cukup");
            break;
        default:
            console.log("Lainnya")
    }
}

testingSwitch()

//Switch berguna untuk menghandling nilai sebuah statement.