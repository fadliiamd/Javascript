var x = 10;
var y = 11;

console.log(x>y) // Lebih besar dari
console.log(x<y) // Kurang dari
console.log(x>=y) // Lebih besar sama dengan dari
console.log(x<=y) // Kurang dari sama dengan
console.log(x==y) // Sama dengan
console.log(x===y) // Sama dengan type data sama
console.log(x!=y) // Tidak sama dengan
console.log(x!==y) // Tidak sama dengan type data sama
